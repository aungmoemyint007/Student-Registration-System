    private void AddNewUser(String uname, String pass, String rank) throws SQLException {
         Connection con;
     PreparedStatement pstat;
     con=DriverManager.getConnection("jdbc:sqlite:D:\\sqlite\\studentdb.db");
     String sql = "insert into user values(?,?,?,?)";
     pstat = con.prepareStatement(sql);
     pstat.setString(1, uname);
     pstat.setString(2, rank);
     pstat.setString(3, pass);
     pstat.setString(4, AddDate());
     pstat.executeUpdate();
     con.close();
    }

   public String AddDate() {
      // SimpleDateFormat dformat = new SimpleDateFormat("MM-dd-YYYY");
       SimpleDateFormat dformat = new SimpleDateFormat("yyyy-MM-dd");
       
       Date dd = new Date();
       return dformat.format(dd);
    }
