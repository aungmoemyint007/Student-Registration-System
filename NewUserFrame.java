    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String uname = txtNewUserName.getText();
        String pass = txtNewPassword.getText();
        String cpass = txtNewConPassword.getText();
        String rank = (String)cbNewRank.getSelectedItem();
        if(uname.length()==0)
            JOptionPane.showMessageDialog(this, "Please type the user name");
        else if(pass.length()==0)
            JOptionPane.showMessageDialog(this, "Please type the password");
         else if(rank.length()==0)
            JOptionPane.showMessageDialog(this, "Please choose Rank");
         else if(cpass.length()==0)
            JOptionPane.showMessageDialog(this, "Please type Confrim Password");
         else if((pass.equals(cpass))==false)
             JOptionPane.showMessageDialog(this,"Password and Confrim Password are not matched");
         else{
            try {
                AddNewUser(uname,pass,rank);
                JOptionPane.showMessageDialog(this, "New User Creation is Success");
                new LoginFrame().setVisible(true);
                this.setVisible(false);
            } catch (SQLException ex) {
                Logger.getLogger(NewUserFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
         }
    }//GEN-LAST:event_btnAddActionPerformed

